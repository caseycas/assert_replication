
public class Contribution {
	public int commits;
	public int total_asserts_add;
	public int total_asserts_del;
	public int total_lines_add;
	public int total_lines_del;
	
	public Contribution()
	{
		commits = 0;
		total_asserts_add = 0;
		total_asserts_del = 0;
		total_lines_add = 0;
		total_lines_del = 0;
	}
	
}
